% --- Executes on button press in proses.
function proses_Callback(hObject, eventdata, handles)
% hObject    handle to proses (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
opts = detectImportOptions('DATA RUMAH.xlsx');
opts.SelectedVariableNames = [3:8];
Data = readmatrix('DATA RUMAH.xlsx',opts);
k=[0,1,1,1,1,1];%nilai atribut, dimana 0= atribut biaya &1= atribut keuntungan
w=[0.30,0.20,0.23,0.10,0.07,0.10];% bobot untuk masing-masing kriteria
[m n]=size (Data); %matriks m x n dengan ukuran sebanyak variabel x(input)
R=zeros (m,n); %membuat matriks R, yang merupakan matriks kosong
Y=zeros (m,n); %membuat matriks Y, yang merupakan titik kosong
for j=1:n,
    if k(j)==1, %statement untuk kriteria dengan atribut keuntungan
        R(:,j)=Data(:,j)./max(Data(:,j));
    else
        R(:,j)=min(Data(:,j))./Data(:,j);
    end;
end;

%tahap perankingan
for i=1:m,
    V(i)= sum(w.*R(i,:))
end;
[sd,no]=sort(V,'descend');
sd;
%sorted data
no;

hasil1 = sd(:,1:20);
hasil2 = no(:,1:20);
hasil3 = [1:20;hasil2;hasil1];

set(handles.uitable2,'Data',(hasil3).')
%menampilkan hasil ke table

